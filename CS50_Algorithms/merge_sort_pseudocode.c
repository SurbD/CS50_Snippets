// MERGE SORT ALGORITHM PSEUDOCODE
//
//  If only one number
//      Quit
//  Else
//      Sort left half of numbers
//      Sort right half of numbers
//      Merge sorted halves
//
//  Visualising merge sort with array:
// >>> [7, 2, 5, 4, 1, 6, 0, 3]
//
//      7 | 2 | 5 | 4 | 1 | 6 | 0 | 3
//      2   7 | 4   5 | 1   6 | 0   3
//      2   4   5   7 | 0   1   3   6
//      0   1   2   3   4   5   6   7
//
// O(nlogn) -- Merge Sort
//
//
// Check out Comparison Sorting Algorithm Visualization:
// https://www.cs.usfca.edu/~galles/visualization/ComparisonSort.html
